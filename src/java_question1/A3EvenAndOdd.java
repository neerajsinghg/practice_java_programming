package java_question1;

public class A3EvenAndOdd {

	public static void main(String[] args) {
		int no = 60;
		
		
		System.out.print("even no :  ");
		for(int i=10; i<=no; i+=2) {
			System.out.print(i +" ");
		}
		System.out.print("\nodd no : ");
		for(int i=11; i<=no; i+=2) {
			System.out.print(i+ " ");
		}
		
	}

}
